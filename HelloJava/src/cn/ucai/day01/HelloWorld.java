package cn.ucai.day01;

/**
 * ctrl + f11 : 运行程序
 * 
 * ctrl + / : 添加或取消单行注释
 * ctrl + shift + / ： 添加多行注释
 * ctrl + shift + \ ： 取消多行注释
 */
public class HelloWorld {
	public static void main(String[] args) {
		// 输出双引号中的内容
		System.out.println(/*"Hello World !!"*/);
		System.out.println("Hello World !!");
		System.out.println("Hello World !!");
		System.out.println("Hello World !!");
		System.out.println("Hello World !!");
		System.out.println("Hello World !!");
	}
}
